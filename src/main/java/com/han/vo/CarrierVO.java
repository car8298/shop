package com.han.vo;

public class CarrierVO {
	
	private String carrCode;
	private String carrName;
	private String carrRef;
	private String carrTel;
	
	public String getCarrCode() {
		return carrCode;
	}
	public void setCarrCode(String carrCode) {
		this.carrCode = carrCode;
	}
	public String getCarrName() {
		return carrName;
	}
	public void setCarrName(String carrName) {
		this.carrName = carrName;
	}
	public String getCarrRef() {
		return carrRef;
	}
	public void setCarrRef(String carrRef) {
		this.carrRef = carrRef;
	}
	public String getCarrTel() {
		return carrTel;
	}
	public void setCarrTel(String carrTel) {
		this.carrTel = carrTel;
	}

}
